def evaluate_movesets(poke_list):
    summary = ''
    for pokemon in poke_list:
        good_moves = 0
        for move in cool_moves:
            for pkmn_mv in pokemon[-1]:
                if move in pkmn_mv[0] and int(pkmn_mv[1]) < 40:
                    good_moves += 1
        good_move_frac = good_moves*1.0/len(pokemon[-1])
        if good_move_frac > 0.6 and pokemon[1] in cool_pkmn:
            summary += pokemon[1] + ': ' + str(good_moves*1.0/len(pokemon[-1])) + '\n'
    return summary
